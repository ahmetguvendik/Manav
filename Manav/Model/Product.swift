import Foundation

class Product {
    var name : String
    var brand : String
    var stock : String
    var price : String
    
    init(name: String, brand: String, stock: String, price: String) {
        self.name = name
        self.brand = brand
        self.stock = stock
        self.price = price
    }
    
}
